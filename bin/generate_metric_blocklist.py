#!/usr/bin/env python
"""
Print a yaml-formatted list of metrics that were removed from the source before a fixed date
(currently 2025-01-01).  This only looks at metrics in the metrics ping.
"""
import datetime
from collections import defaultdict

import requests
import yaml

header = """# generated by bin/generate_metric_blocklist.py
# metrics per app or library to exclude from output
# blocklist does not handle individual pings for metrics with "all_pings" in send_in_pings
# keys are the app/library name (e.g. fenix), not the app id (org-mozilla-firefox)"""

url_template = "https://probeinfo.telemetry.mozilla.org/glean/{app}/{type}"

app_listings = requests.get(
    "https://probeinfo.telemetry.mozilla.org/v2/glean/app-listings"
).json()
v1_to_app_name = {app["v1_name"]: app["app_name"] for app in app_listings}
# one entry per app name, i.e. one channel
app_name_to_v1 = {v: k for k, v in v1_to_app_name.items()}

dependencies = {
    v["name"]
    for v1_name in v1_to_app_name.keys()
    for v in requests.get(url_template.format(app=v1_name, type="dependencies"))
    .json()
    .values()
}

apps = list(app_name_to_v1.values()) + [
    v["name"]
    for v in requests.get(
        "https://probeinfo.telemetry.mozilla.org/glean/repositories"
    ).json()
    if dependencies.intersection(v.get("library_names", []))
]

removed = defaultdict(lambda: defaultdict(list))

while apps:
    app = apps.pop()
    metrics = requests.get(url_template.format(app=app, type="metrics")).json()
    for name, info in metrics.items():
        if (
            datetime.datetime.fromisoformat(info["history"][-1]["dates"]["last"])
            < datetime.datetime(2025, 1, 1)
            and info["in-source"] is False
            and info["type"] != "event"
        ):
            for send_in_ping in info["history"][-1]["send_in_pings"]:
                if send_in_ping == "metrics":
                    removed[v1_to_app_name.get(app, app)][send_in_ping].append(name)

yaml.Dumper.add_representer(defaultdict, yaml.representer.Representer.represent_dict)

print(header)
print(yaml.dump(removed))
